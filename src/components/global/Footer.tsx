import Link from 'next/link';
import { getContentProvider } from '@/infrastructure';
import type { FooterConfig } from '@/core/content/types';

async function getFooterConfig(): Promise<FooterConfig | null> {
  const provider = getContentProvider();
  const site = await provider.getSite();
  return site?.globals?.footer ?? null;
}

export async function Footer() {
  const config = await getFooterConfig();

  if (!config) {
    return null;
  }

  const { logo, description, linkGroups, copyright, social } = config;

  return (
    <footer className="bg-gray-900 text-gray-300">
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-12">
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-8">
          {/* Brand column */}
          <div className="lg:col-span-1">
            {logo && (
              <div className="flex items-center gap-2 mb-4">
                {logo.src ? (
                  <img src={logo.src} alt={logo.text || 'Logo'} className="h-8 w-auto" />
                ) : (
                  <span className="text-xl font-semibold text-white">{logo.text}</span>
                )}
              </div>
            )}
            {description && (
              <p className="text-sm text-gray-400 mb-6">{description}</p>
            )}
            {social && social.length > 0 && (
              <div className="flex gap-4">
                {social.map((item) => (
                  <a
                    key={item.href}
                    href={item.href}
                    target="_blank"
                    rel="noopener noreferrer"
                    className="text-gray-400 hover:text-white transition-colors"
                    aria-label={item.platform}
                  >
                    <SocialIcon platform={item.platform} />
                  </a>
                ))}
              </div>
            )}
          </div>

          {/* Link groups */}
          {linkGroups?.map((group) => (
            <div key={group.title}>
              <h3 className="text-sm font-semibold text-white uppercase tracking-wider mb-4">
                {group.title}
              </h3>
              <ul className="space-y-3">
                {group.links.map((link) => (
                  <li key={link.href}>
                    <Link
                      href={link.href}
                      className="text-sm text-gray-400 hover:text-white transition-colors"
                    >
                      {link.label}
                    </Link>
                  </li>
                ))}
              </ul>
            </div>
          ))}
        </div>

        {/* Copyright */}
        {copyright && (
          <div className="mt-12 pt-8 border-t border-gray-800">
            <p className="text-sm text-gray-500 text-center">{copyright}</p>
          </div>
        )}
      </div>
    </footer>
  );
}

